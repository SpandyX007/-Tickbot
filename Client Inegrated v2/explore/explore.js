// Search button event listener

document.getElementById('search-button').addEventListener('click', () => {
  let input = document.querySelector('.search-container>input');
  let searchValue = input.value.toLowerCase();

  // Filter the data based on the search input
  let matchingMuseums = datas.filter((data) => {
    return data.location.city.toLowerCase() === searchValue;
  });

  let cardsContainer = document.querySelector('.cards-container');
  cardsContainer.innerHTML = ''; // Clear previous results

  matchingMuseums.forEach(museum => {
    let card = document.createElement('div');
    card.className = 'card';
    card.innerHTML = `
      <img src="${museum.image}" alt="${museum.title}">
      <div class="card-title">${museum.title}</div>
      <div class="card-description">${museum.description}</div>
      <div class="card-timing">
        Open: ${museum.timing.open} - Close: ${museum.timing.close} <br>
        Days: ${museum.timing.days_open.join(', ')}
      </div>
      <div class="card-location">
        ${museum.location.address}, ${museum.location.city}, ${museum.location.state}, ${museum.location.country}
      </div>
      <div class="card-fare">
        Adult: ₹${museum.entrance_fee.adult} <br>
        Child: ₹${museum.entrance_fee.child} <br>
        Foreigner: ₹${museum.entrance_fee.foreigner}
      </div>
    `;
    cardsContainer.appendChild(card);
  });
});
